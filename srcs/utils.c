#include "../includes/pipex.h"
#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>

void	ft_strdel(char	**str) // a implementer dans libft
{
	if (*str)
	{
		free(str);
		*str = NULL;
	}
}

void	ft_putchar_fd(char c, int fd)
{
	if (fd >= 0)
		write(fd, &c, 1);
}

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			ft_putchar_fd(s[i], fd);
			i++;
		}
	}
}
